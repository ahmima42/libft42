#include "libft.h"

int	ft_isalpha(int c)
{
	unsigned char	cr;

	if (c < 0 || c > 255)
		return (0);
	cr = (unsigned char) c;
	if ((cr <= 'z' && cr >= 'a') || (cr <= 'Z' && cr >= 'A'))
		return (1);
	return (0);
}
