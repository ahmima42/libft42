#include "libft.h"

int	ft_isdigit(int c)
{
	unsigned char	cr;

	if (c < 0 || c > 255)
		return (0);
	cr = (unsigned char) c;
	if (cr >= '0' && cr <= '9')
		return (1);
	return (0);
}
