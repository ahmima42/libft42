#include "libft.h"

int	ft_toupper(int c)
{
	unsigned char	cr;

	if (c < 0 || c > 255)
		return (c);
	cr = (unsigned char) c;
	if (cr >= 'a' && cr <= 'z')
		cr -= 32;
	return (cr);
}
